public class TestaSwitch {
	public static void main(String[] args) {

		int mes = 3;

		switch (mes) {
		case 1:
			System.out.println("É janeiro zé!");
			break;
		case 2:
			System.out.println("É Fevereiro zé");
			break;
		default:
			System.out.println("coloca um mes certo candango");
			break;
		}

	}
}
